package proyecto;

public class Alumnos {

	private String nombre;
	private String apellido;
	private String dni;
	private String localidad;
	
	public Alumnos(String nombre, String apellido, String dni, String localidad) {
		this.nombre = nombre;
		this.apellido = apellido;
		this.dni = dni;
		this.localidad = localidad;
	}
}